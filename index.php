      <h2>¿Que es negociacion?</h2>    
      
      <p>En el contexto de la ingeniería de software, la negociación se refiere a un proceso en el que dos o más partes involucradas en un proyecto de software discuten y acuerdan los términos, requisitos y compromisos del proyecto.</p>

     <p>La negociación es una habilidad importante en la ingeniería de software porque permite a las partes involucradas llegar a un acuerdo sobre los objetivos, el alcance, el presupuesto, el calendario y otros aspectos importantes del proyecto. En muchos casos, las partes involucradas en un proyecto de software tienen objetivos y prioridades diferentes, y la negociación ayuda a alinear estas prioridades y llegar a un acuerdo que sea beneficioso para todas las partes.</p> 
      
     <p>La negociación en la ingeniería de software también puede involucrar la discusión y el acuerdo sobre temas como la propiedad intelectual, la protección de datos, las responsabilidades legales y otros aspectos importantes del proyecto.</p> 
      
      <p>Para tener éxito en la negociación en la ingeniería de software, es importante tener una comprensión clara de los requisitos y objetivos del proyecto, tener habilidades de comunicación efectivas y ser capaz de llegar a un acuerdo que sea justo y equitativo para todas las partes involucradas.</p>  

    <h3>Los términos mas comunes que se usan a la hora de negociar un contrato de software son los siguientes: </h3>

      <p> <b> Alcance: </b>se refiere a los límites del proyecto de software y lo que se espera que haga.</p>

      <p>Entregables: son los productos o resultados finales que se espera que se entreguen en el proyecto de software.</p>

      <p> <b> Plazo: </b>es el período de tiempo acordado para completar el proyecto.</p>

      <p> <b> Precio: </b>el costo total del proyecto de software.</p>

      <p> <b>Mantenimiento y soporte: </b>  el tipo y nivel de soporte que se proporcionará después de que se entregue el software.</p>

      <p> <b> Propiedad intelectual: </b>la propiedad de los derechos de autor, patentes y otras formas de propiedad intelectual del software.</p>

      <p> <b> Responsabilidades y obligaciones: </b>las responsabilidades y obligaciones de ambas partes durante el proyecto.</p>

      <p> <b> Penalizaciones por incumplimiento: </b>las consecuencias si alguna de las partes no cumple con los términos del contrato.</p>

      <p> <b>Confidencialidad:</b>  las medidas de seguridad para garantizar la confidencialidad de los datos y la información del proyecto.</p>

      <p> <b> Garantías: </b>las garantías de indole del software entregado.</p>

    <h3>A la hora de tener una negociacion tenemos que enfrentarnos a a ciertas desiciones a la hora de escoger la manera en que queremos hacerlo. A continuacion te voy a explicar cuales son esas posibles decisiones: </h3>

        <p>AHP (Analytic Hierarchy Process)</p>
        <p>DEA (Data Envelopment Analysis)</p>
        <p>TCO (Total Cost of Ownership)</p>
        <p>TIR (Tasa Interna de Retorno)</p>

    <h2>¿Que es AHP?</h2>

    <p>AHP (Analytic Hierarchy Process) es una metodología de análisis multicriterio que se utiliza para tomar decisiones complejas en una amplia variedad de campos, incluyendo la ingeniería de software. Fue desarrollada por el matemático estadounidense Thomas Saaty en la década de 1970. </p>

    <p>El método AHP permite a los tomadores de decisiones evaluar y comparar múltiples criterios y alternativas de manera estructurada y sistemática. A través de un proceso de jerarquización y ponderación, se pueden determinar las prioridades de los criterios y las alternativas en función de su importancia relativa.</p>  
      
    <p>El proceso AHP consta de varias etapas, incluyendo la identificación de los criterios y alternativas relevantes, la creación de una matriz de comparación, la asignación de pesos relativos a los criterios y la determinación de la solución óptima a través de la agregación de las prioridades calculadas.</p>  
      
     <p>La metodología AHP se utiliza en la ingeniería de software para la toma de decisiones en diversas áreas, como la selección de herramientas y tecnologías, la priorización de requisitos, la evaluación de proveedores y la planificación de proyectos.</p> 

    <h2>¿Que es DEA?</h2>

    <p>DEA (Data Envelopment Analysis) es una técnica de análisis de eficiencia que se utiliza en la ingeniería de software y otros campos. La DEA es un método matemático que se utiliza para evaluar la eficiencia relativa de un conjunto de unidades (por ejemplo, empresas, departamentos, proyectos) en la utilización de sus recursos para producir resultados. </p>

    <p>La DEA se basa en la idea de que una unidad es eficiente si puede producir los mismos resultados que otras unidades utilizando menos recursos o si puede producir más resultados utilizando los mismos recursos. La DEA utiliza técnicas estadísticas y matemáticas para medir la eficiencia de las unidades en términos de entradas y salidas.</p>  
      
    <p>La DEA se utiliza comúnmente en la ingeniería de software para evaluar la eficiencia de los equipos de desarrollo de software y otros aspectos de los procesos de desarrollo de software. Por ejemplo, la DEA se puede utilizar para evaluar la eficiencia de los equipos de desarrollo en términos de su capacidad para producir software de alta aptitud en un plazo determinado y con un presupuesto específico.</p>  
      
     <p>En resumen, DEA es una técnica de análisis de eficiencia que se utiliza en la ingeniería de software y otros campos para evaluar la eficiencia relativa de las unidades en la utilización de sus recursos para producir resultados. La DEA se basa en técnicas estadísticas y matemáticas para medir la eficiencia de las unidades en términos de entradas y salidas.</p> 

    <h2>¿Que es TCO?</h2>

    <p>TCO (Total Cost of Ownership) se refiere al costo total de propiedad de un sistema o aplicación de software durante su ciclo de vida completo. Esto incluye no solo el costo inicial de adquisición, sino también los costos de operación, mantenimiento, soporte y eventualmente la eliminación del sistema.</p>

     <p>El TCO se utiliza comúnmente en la ingeniería de software para evaluar el costo total de un proyecto de software y para tomar decisiones informadas sobre la adquisición y el mantenimiento de sistemas de software. Es especialmente útil para comparar diferentes soluciones de software y determinar cuál tiene el costo total de propiedad más bajo a largo plazo.</p> 
      
    <p> Para calcular el TCO de un sistema de software, se deben considerar los costos relacionados con la adquisición de hardware y software, la instalación, la capacitación del usuario, el soporte técnico, el mantenimiento, los upgrades, el reemplazo y la eliminación del sistema. Es importante tener en cuenta que los costos pueden variar según el tipo de sistema de software y la organización que lo adquiere.</p> 
      
    <p>En resumen, TCO (Total Cost of Ownership) se refiere al costo total de propiedad de un sistema o aplicación de software durante su ciclo de vida completo. El TCO se utiliza en la ingeniería de software para evaluar el costo total de un proyecto de software y para tomar decisiones informadas sobre la adquisición y el mantenimiento de sistemas de software. Para calcular el TCO, se deben considerar los costos relacionados con la adquisición, instalación, capacitación, soporte técnico, mantenimiento, upgrades, reemplazo y eliminación del sistema.</p>  

    <h2>¿Que es TIR?</h2>

    <p>TIR (Tasa Interna de Retorno) es una medida financiera utilizada para evaluar la rentabilidad de un proyecto de inversión. La TIR es la tasa de descuento a la cual el valor presente de los flujos de efectivo futuros del proyecto es igual al costo inicial de inversión.</p>

     <p>En la ingeniería de software, la TIR se utiliza para evaluar la rentabilidad de proyectos de desarrollo de software y para comparar diferentes opciones de inversión. Un proyecto de desarrollo de software es considerado rentable si su TIR es mayor que la tasa de descuento utilizada para evaluarlo.</p> 
      
     <p>El cálculo de la TIR implica la estimación de los flujos de efectivo futuros del proyecto, incluyendo ingresos y costos, y la determinación de una tasa de descuento apropiada que refleje el costo de oportunidad de los recursos invertidos en el proyecto. Si el valor presente de los flujos de efectivo futuros es igual al costo inicial de inversión, entonces la tasa de descuento utilizada se convierte en la TIR del proyecto.</p> 
      
     <p>La TIR se utiliza junto con otras medidas financieras, como el Valor Presente Neto (VPN), para evaluar la rentabilidad de los proyectos de desarrollo de software y para tomar decisiones informadas sobre la inversión en proyectos de software.</p> 
      
     <p>En resumen, la TIR (Tasa Interna de Retorno) es una medida financiera utilizada para evaluar la rentabilidad de un proyecto de inversión. En la ingeniería de software, la TIR se utiliza para evaluar la rentabilidad de proyectos de desarrollo de software y para comparar diferentes opciones de inversión. El cálculo de la TIR implica la estimación de los flujos de efectivo futuros del proyecto y la determinación de una tasa de descuento apropiada. La TIR se utiliza junto con otras medidas financieras, como el Valor Presente Neto (VPN), para evaluar la rentabilidad de los proyectos de desarrollo de software y tomar decisiones informadas sobre la inversión en proyectos de software.</p>
